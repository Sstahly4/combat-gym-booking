export function PaymentMethodMark({
  method,
  className,
}: {
  method: PaymentMethodChoice
  className?: string
}) {
  if (method === 'card') {
    return <CreditCard className={className ?? 'h-5 w-5 text-gray-900'} strokeWidth={1.75} />
  }
  if (method === 'google_pay') {
    return <GooglePayMark className={className ?? 'h-5 w-auto'} />
  }
  return <ApplePayMark className={`text-gray-900 ${className ?? 'h-5 w-auto'}`} />
}
